public class Calculadora {

    public int getSoma(int valorA, int valorB) {

        return valorA + valorB;
    }

    public int getSubtracao(int valorA, int valorB) {

        return valorA - valorB;
    }

    public int getMultiplicacao(int valorA, int valorB) {

        return valorA * valorB;
    }

    public int getDivisao(int valorA, int valorB) {

        return valorA/valorB;
    }
}